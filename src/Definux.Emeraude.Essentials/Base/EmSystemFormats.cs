namespace Definux.Emeraude.Essentials.Base
{
    /// <summary>
    /// Default system formats in the Emeraude.
    /// </summary>
    public static class EmSystemFormats
    {
        /// <summary>
        /// Default date format used by Emeraude.
        /// </summary>
        public const string DateFormat = "dddd, dd/MM/yyyy";

        /// <summary>
        /// Default time format used by Emeraude.
        /// </summary>
        public const string TimeFormat = @"hh\:mm";

        /// <summary>
        /// Default date time format used by Emeraude.
        /// </summary>
        public const string DateTimeFormat = "dddd, dd/MM/yyyy HH:mm";

        /// <summary>
        /// Default short date format used by Emeraude.
        /// </summary>
        public const string ShortDateFormat = "dd/MM/yyyy";

        /// <summary>
        /// Default date separator used by Emeraude.
        /// </summary>
        public const string DateSeparator = "/";
    }
}
